package ru.hackathon;

import lombok.extern.slf4j.Slf4j;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

import java.util.ArrayList;
import java.util.List;

@Slf4j
@RestController("test")
public class ExampleController {

    private final List<String> food = new ArrayList<>();
    @GetMapping("/eat-ram")
    public void eatRam() {
        log.info(">> start eating ram");
        while (true) {
            food.add("LARGE MESSAGE --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
        }
    }
    @GetMapping("/fast")
    public String fast() {
        log.info(">> fast execution");
        return "Hello from Controller";
    }
    @GetMapping("/slow")
    public String slow(@RequestParam Integer sleepSeconds) throws Exception {
        log.info(">> slow execution with sleeping for " + sleepSeconds + " sec");
        Thread.sleep(sleepSeconds*1000);
        return "sleep complete in " + sleepSeconds + " sec";
    }
}